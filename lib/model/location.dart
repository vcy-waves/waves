class Location {
  String name;
  String city;
  String district;
  String picture;
  String village;
  String street;
  String streetNumber;
  String fullAddress;
  double rate;
  Location({
    required this.name,
    required this.city,
    required this.district,
    required this.fullAddress,
    required this.street,
    required this.streetNumber,
    required this.village,
    required this.picture,
    required this.rate,
  });
}
